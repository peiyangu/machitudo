const genreNames = {
  food: "フード",
  sweets: "スイーツ",
  drink: "ドリンク",
  workshop: "ワークショップ",
  festival: "縁日",
  sales: "物販",
  green: "グリーン",
  other: "その他"
};

// 日付のフォーマット関数
function formatDays(days) {
  if (days.includes("1/31") && days.includes("2/1")) {
    return "両日開催";
  } else if (days.includes("1/31")) {
    return "1/31(土)のみ";
  } else if (days.includes("2/1")) {
    return "2/1(日)のみ";
  }
  return "";
}

// ジャンル別に店舗を取得する関数
function getStoresByGenre(genreKey) {
  // genreKeyを日本語名に変換
  const genreName = genreNames[genreKey] || genreKey;
  return allStoresData.filter(store => store.genre === genreName);
}

// ランダムに店舗を取得する関数（PICK UP用）
function getRandomStores(count = 5) {
  const shuffled = [...allStoresData].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
